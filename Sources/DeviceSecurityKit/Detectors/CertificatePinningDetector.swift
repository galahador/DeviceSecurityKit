import Foundation
import Darwin
import CFNetwork
import MachO

public final class CertificatePinningDetector {

    private static let logger = SecurityLogger.security(subsystem: "CertificatePinningDetector")
    private static let o = StringObfuscator.shared

    // MARK: - Public
    public static func isPinningBypassed() -> Bool {
        return checkSecurityFrameworkIntegrity()
            || checkSSLBypassLibraries()
            || checkProxyConfiguration()
    }

    private static func checkSecurityFrameworkIntegrity() -> Bool {
        let functions = [
            // Existing — SSLHandshake, SSLSetSessionTicketsEnabled, SSLSetIOFuncs
            o.reveal([0xB4, 0xEB, 0xE9, 0xD8, 0x88, 0x22, 0x10, 0xFB, 0x7F, 0x4C, 0x69, 0x71, 0x7C, 0x06, 0x69, 0x35, 0x25, 0x45, 0xAC, 0x50]),
            o.reveal([0x1C, 0xEE, 0xB0, 0x60, 0x65, 0x11, 0x03, 0xEC, 0x42, 0xDB, 0xC2, 0x52, 0x21, 0xE9, 0xE5, 0x68,
                      0x69, 0x7A, 0xD0, 0xCB, 0x1B, 0xA0, 0x99, 0x47, 0x65, 0xAD, 0xA8, 0xB2, 0x78]),
            o.reveal([0x38, 0xB8, 0x76, 0x28, 0x6B, 0xA5, 0xA6, 0xE2, 0x1B, 0x1A, 0xD7, 0x8F, 0x56, 0xFC, 0xFB, 0x93]),
            // SecTrust — primary targets for pinning bypass tools
            o.reveal([0xE4, 0x50, 0xC6, 0x89, 0xBC, 0xE7, 0x50, 0xF2, 0x6B, 0xB5, 0xAB, 0x44, 0xAA, 0xBC, 0xBC, 0xF6, 0xF1, 0x0B, 0x49, 0x93, 0x58, 0x09, 0x9B, 0xF8, 0xE2, 0x23, 0x26, 0x15, 0x8F]),
            o.reveal([0xE5, 0xC3, 0x24, 0xA8, 0x10, 0xA6, 0xFE, 0x5F, 0x07, 0x78, 0xB9, 0x25, 0x60, 0x68, 0xE7, 0x97, 0x03, 0xDA, 0xDB, 0xAB, 0xF6, 0xA4, 0xDD, 0xFF, 0x2B, 0x06, 0xF3, 0x7A, 0x8C, 0xE9, 0x14, 0xF0, 0x95]),
            o.reveal([0xCA, 0x92, 0x71, 0x99, 0x12, 0xF4, 0x58, 0x0D, 0x71, 0xEE, 0x71, 0xC3, 0xAA, 0x18, 0xDF, 0x1D, 0xF9, 0xD6, 0xEC, 0x83, 0x84, 0x1F]),
            o.reveal([0x10, 0xE5, 0xC1, 0x82, 0x5C, 0x0E, 0xB0, 0x93, 0x77, 0xC4, 0xF5, 0x19, 0xF6, 0xE7, 0xC6, 0x0F, 0x71, 0x62, 0x47, 0xA0, 0x05, 0x64, 0x34]),
            o.reveal([0x2A, 0x44, 0x64, 0x19, 0xA0, 0x27, 0x6C, 0xFA, 0x8F, 0x7D, 0xCB, 0x88, 0x9E, 0x0E, 0xA2, 0x44, 0x37, 0x91, 0x13, 0xE5, 0xBE, 0x7F, 0xEC, 0x0C, 0x52]),
        ]

        let expectedPrefix = o.reveal([0xCE, 0x16, 0xC4, 0xB0, 0x81, 0xB5, 0xE9, 0xD1, 0x1C, 0x8D, 0x2C, 0x17,
            0x00, 0xDA, 0x0C, 0xD8, 0x1D, 0x85, 0xF7, 0x33, 0xB0, 0x51, 0x30, 0x6E,
            0x63, 0x06, 0x41, 0xA3, 0x1B, 0xC3, 0xA4, 0xB3, 0x06, 0x83, 0x94, 0x27,
            0x8E, 0xC6, 0x5A, 0x83, 0xA4, 0x83, 0x49, 0xF8, 0x88, 0xC9, 0x78, 0x5A,
            0x57])

        guard let handle = dlopen(nil, RTLD_NOW) else { return false }
        defer { dlclose(handle) }

        for name in functions {
            guard let sym = dlsym(handle, name) else { continue }

            var info = Dl_info()
            guard dladdr(sym, &info) != 0, let fname = info.dli_fname else {
                logger.warning("Pinning check: cannot resolve image for \(SecurityLogger.redact(name)) — treating as suspicious")
                return true
            }

            let imagePath = String(cString: fname)
            if !SystemImageValidator.shared.normalizedImagePath(imagePath).hasPrefix(expectedPrefix) {
                logger.warning("Pinning bypass: \(SecurityLogger.redact(name)) redirected to: \(SecurityLogger.redact(imagePath))")
                return true
            }
        }

        if checkURLSessionDelegateSwizzled() {
            return true
        }

        return false
    }

    private static func checkURLSessionDelegateSwizzled() -> Bool {
        let sessionClass = o.reveal([0x41, 0xC5, 0x4B, 0xEF, 0x44, 0x17, 0x79, 0x2A, 0xA4, 0x99, 0x82, 0x69, 0x4F, 0x90, 0x11, 0xEC])
        let challengeSel = o.reveal([0x75, 0x0B, 0x11, 0x1E, 0x14, 0xFC, 0xC9, 0xF9, 0x0A, 0x13, 0xA7, 0x29, 0x76, 0xC0, 0xD7, 0x88,
                                     0xDD, 0x3A, 0xE7, 0x99, 0xF2, 0xB3, 0x48, 0xF2, 0x2A, 0x42, 0x4C, 0x83, 0x61, 0x4A, 0xBC, 0x78,
                                     0x43, 0xD5, 0x92, 0x5C, 0x3C, 0xAF, 0x15, 0x12, 0x24, 0x74, 0x9B, 0xC2, 0x0A, 0x18, 0xD0, 0x3E,
                                     0xEA, 0xB0, 0xE3, 0x6C, 0x0A])

        guard let cls = NSClassFromString(sessionClass) else { return false }
        let sel = NSSelectorFromString(challengeSel)
        guard class_getInstanceMethod(cls, sel) != nil,
              let imp = class_getMethodImplementation(cls, sel) else { return false }

        let ptr = unsafeBitCast(imp, to: UnsafeRawPointer.self)
        var info = Dl_info()
        guard dladdr(ptr, &info) != 0, let fname = info.dli_fname else { return true }

        let imagePath = String(cString: fname)
        let appPath = Bundle.main.executablePath ?? ""

        if imagePath != appPath && !SystemImageValidator.shared.isSystemImage(imagePath) {
            logger.warning("URLSession challenge delegate swizzled to: \(SecurityLogger.redact(imagePath))")
            return true
        }

        return false
    }

    private static func checkSSLBypassLibraries() -> Bool {
        let bypassLibraries = [
            o.reveal([0x03, 0xF1, 0xCC, 0xD3, 0xB9, 0xF6, 0x62, 0x9A, 0xF1, 0x87, 0xBF, 0x24, 0x93, 0x05, 0x40, 0x39, 0x53, 0xB2]),
            o.reveal([0xAC, 0x3A, 0x5F, 0xC7, 0x54, 0x06, 0xB9, 0xC4, 0x2E, 0x86, 0xCC, 0xFF, 0xB2, 0x90, 0xEC, 0xD7, 0x85, 0xE8, 0x4F]),
            o.reveal([0x79, 0xA1, 0x76, 0xD3, 0x5B, 0x65, 0xD9, 0x9C, 0x51, 0x64, 0x2B, 0x15, 0x11, 0xF5, 0x26, 0x1F, 0xF2, 0x56])
        ]

        let imageCount = _dyld_image_count()
        for i in 0..<imageCount {
            guard let rawName = _dyld_get_image_name(i) else { continue }
            let imageName = String(cString: rawName).lowercased()

            for lib in bypassLibraries {
                if imageName.contains(lib.lowercased()) {
                    logger.warning("SSL bypass library detected: \(SecurityLogger.redact(imageName))")
                    return true
                }
            }
        }

        return false
    }

    private static func checkProxyConfiguration() -> Bool {
        return ProxyConfigurationChecker.isProxyConfigured()
    }
}
