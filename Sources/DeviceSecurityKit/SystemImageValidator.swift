import Foundation

/// Single source of truth for "does this Mach-O image path point into an Apple system location
internal struct SystemImageValidator {

    internal static let shared = SystemImageValidator()

    private let systemPrefixes: [String]

    private init() {
        let o = StringObfuscator.shared
        systemPrefixes = [
            o.reveal([0xA9, 0xBE, 0x53, 0x9D, 0xDE, 0x11, 0x60, 0x66, 0xDC, 0x8A, 0x87, 0xDC, 0xEC]),
            o.reveal([0x62, 0x2E, 0x26, 0x13, 0x48, 0x9F, 0xA6, 0xE7, 0x29, 0xAB, 0x7D, 0xD1, 0x63, 0x9D, 0x63, 0x58, 0xBB, 0x06, 0x9C, 0x39]),
            o.reveal([0x28, 0x24, 0x55, 0xF6, 0x2D, 0xFB, 0x67, 0xF1, 0x82, 0x24, 0xAF, 0x48, 0x6F, 0x99, 0x76, 0x40, 0x75, 0x29, 0x95]),
        ]
    }

    internal func isSystemImage(_ path: String) -> Bool {
        systemPrefixes.contains { path.hasPrefix($0) }
    }
}
